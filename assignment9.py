# List Operations

numbers = [10, 20, 30, 40, 50]

print("List:", numbers)

# Sum of List
total = 0
for i in numbers:
    total += i
print("Sum of List =", total)

# Product of List
product = 1
for i in numbers:
    product *= i
print("Product of List =", product)

# Maximum Element
maximum = max(numbers)
print("Maximum =", maximum)

# Minimum Element
minimum = min(numbers)
print("Minimum =", minimum)

# Length of List
length = len(numbers)
print("Length =", length)